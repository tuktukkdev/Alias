-- Link tags to collections
WITH collections AS (
  SELECT id, name
  FROM "default_collections"
  WHERE name IN ('Еда', 'Животные')
),
required_links AS (
  SELECT c.id AS collection_id, t.id AS tag_id
  FROM collections c
  JOIN "tags" t
    ON (
      c.name = 'Еда' AND t.name IN ('Еда', 'Продукты', 'Готовка')
    )
    OR (
      c.name = 'Животные' AND t.name IN ('Животные', 'Питомцы', 'Звери', 'Природа')
    )
)
INSERT INTO "collection_tags" ("collection_id", "tag_id")
SELECT rl.collection_id, rl.tag_id
FROM required_links rl
LEFT JOIN "collection_tags" ct
  ON ct.collection_id = rl.collection_id AND ct.tag_id = rl.tag_id
WHERE ct.collection_id IS NULL;

